public class Comments {
    public static void main(String[] args) {
        
        // This is a single-line comment
        System.out.println("Hello, this line is after a single-line comment!");

        /*
          This is a multi-line comment.
          It can span across multiple lines.
         */
        System.out.println("This line comes after a multi-line comment.");

        /** 
          This is a documentation comment.
          It is usually used for generating external documentation using Javadoc.
         */
        System.out.println("This line follows a documentation comment.");
    }
}